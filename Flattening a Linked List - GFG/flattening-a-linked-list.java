/*  Function which returns the  root of 
    the flattened linked list. */
class GfG
{
    Node flatten(Node root)
    {
        Node curr1 = root.next;
        while(curr1 != null){
            while(curr1.bottom != null){
                Node down = curr1.bottom;
                Node curr2 = root;
                while(curr2.bottom != null && curr2.data < down.data && curr2.bottom.data <= down.data)
                    curr2 = curr2.bottom;
                
                Node hold = curr1.bottom;
                curr1.bottom = hold.bottom;
                hold.bottom = curr2.bottom;
                curr2.bottom = hold;
            }
            curr1 = curr1.next;
        }
        curr1 = root;
        while(curr1.next != null){
            Node curr2 = curr1;
            while(curr2.bottom != null && curr2.data < curr1.next.data && curr2.bottom.data < curr1.next.data)
                curr2 = curr2.bottom;
            Node hold = curr1.next;
            curr1.next = curr1.next.next;
            hold.next = null;
            hold.bottom = curr2.bottom;
            curr2.bottom = hold;
        }
        return root;
    }
}